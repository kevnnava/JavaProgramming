package day17_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {

        if (true){ // 1 time
            System.out.println("Wooden Spoon");
        }

        System.out.println("------------------------");

        while (true){ // repeated if statement
            System.out.println("Wooden Spoon");
        }
    }
}
