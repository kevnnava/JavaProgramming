package day18_NestedLoop;

public class NestedLoopPractice2 {

    public static void main(String[] args) {

        for (int j = 0; j < 10; j++) {

            for (int i = 0; i < 10; i++) { // j: 0, 1, 2
                System.out.print("* ");
            }

            System.out.println();


        }



    }
}
